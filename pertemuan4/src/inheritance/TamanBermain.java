package inheritance;

/**
 *
 * @author MSI Gaming
 */
public class TamanBermain {
    private String nama_taman;
    private String alamat_taman;

    public TamanBermain(String nama_taman, String alamat_taman) {
        this.nama_taman = nama_taman;
        this.alamat_taman = alamat_taman;
    }

    public String getNama_taman() {
        return nama_taman;
    }

    public void setNama_taman(String nama_taman) {
        this.nama_taman = nama_taman;
    }

    public String getAlamat_taman() {
        return alamat_taman;
    }

    public void setAlamat_taman(String alamat_taman) {
        this.alamat_taman = alamat_taman;
    }

    
}
